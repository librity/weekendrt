#include <libft.h>

/*
** Runs de del function on all of the list's contents, frees every node on the
** list and nulls the start pointer.
*/

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *next;

	if (lst == NULL)
		return ;
	while (*lst != NULL)
	{
		next = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = next;
	}
	lst = NULL;
}
